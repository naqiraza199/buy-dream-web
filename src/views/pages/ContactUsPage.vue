<script setup>
import FooterSection from '@/components/FooterSection.vue'
import NavbarSection from '@/components/NavbarSection.vue'
import { ref } from 'vue'

const formData = ref({
  full_name: '',
  email: '',
  phone: '',
  company: '',
  notes: ''
})

const openFaq = ref(null)
const isSubmitting = ref(false)

const contactFaqs = [
  {
    question: 'How quickly will you respond to my inquiry?',
    answer: 'We typically respond to all inquiries within 24 hours during business days. For urgent projects, we prioritize immediate responses.'
  },
  {
    question: 'What information should I include in my message?',
    answer: 'Please include details about your project, timeline, budget range, and any specific requirements. The more information you provide, the better we can help.'
  },
  {
    question: 'Do you offer free consultations?',
    answer: 'Yes! We offer free initial consultations to discuss your project needs. This helps us understand your requirements and provide an accurate quote.'
  }
]

const handleSubmit = async () => {
  isSubmitting.value = true
  try {
    const response = await fetch('https://jaexlfmjjzpahdmlvhii.supabase.co/functions/v1/submit-lead', {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify(formData.value)
    })
    const result = await response.json()
    if (result.success) {
      showNotification('Thank you! We\'ll be in touch soon.', 'success')
      formData.value = {
        full_name: '',
        email: '',
        phone: '',
        company: '',
        notes: ''
      }
    } else {
      showNotification('Something went wrong. Please try again.', 'error')
    }
  } catch (error) {
    showNotification('Network error. Please try again.', 'error')
  } finally {
    isSubmitting.value = false
  }
}

const showNotification = (message, type) => {
  const notification = document.createElement('div')
  notification.className = `notification notification-${type}`
  notification.innerHTML = `
    <div class="notification-content">
      <i class="fas ${type === 'success' ? 'fa-check-circle' : 'fa-exclamation-circle'}"></i>
      <span>${message}</span>
    </div>
  `
  document.body.appendChild(notification)
  setTimeout(() => notification.classList.add('show'), 10)
  setTimeout(() => {
    notification.classList.remove('show')
    setTimeout(() => notification.remove(), 300)
  }, 4000)
}

const toggleFaq = (index) => {
  openFaq.value = openFaq.value === index ? null : index
}
</script>

