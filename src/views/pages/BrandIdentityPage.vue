<script>
import {
    ref
} from 'vue';
import NavbarSection from '@/components/NavbarSection.vue';
import FooterSSection from '@/components/FooterSection.vue';

export default {
    name: 'BrandIdentityPage',
    components: {
        NavbarSection,
        FooterSSection
    },
    setup() {
        const quoteForm = ref({
            full_name: '',
            email: '',
            phone: '',
            company: '',
            notes: ''
        });

        const isSubmitting = ref(false);

        const handleQuoteSubmit = async () => {
            isSubmitting.value = true;
            try {
                const response = await fetch('https://jaexlfmjjzpahdmlvhii.supabase.co/functions/v1/submit-lead', {
                    method: 'POST',
                    headers: { 'Content-Type': 'application/json' },
                    body: JSON.stringify(quoteForm.value)
                });
                const result = await response.json();
                if (result.success) {
                    showNotification('Thank you! We\'ll be in touch soon.', 'success');
                    quoteForm.value = {
                        full_name: '',
                        email: '',
                        phone: '',
                        company: '',
                        notes: ''
                    };
                } else {
                    showNotification('Something went wrong. Please try again.', 'error');
                }
            } catch (error) {
                showNotification('Network error. Please try again.', 'error');
            } finally {
                isSubmitting.value = false;
            }
        };

        const showNotification = (message, type) => {
            const notification = document.createElement('div');
            notification.className = `notification notification-${type}`;
            notification.innerHTML = `
                <div class="notification-content">
                    <i class="fas ${type === 'success' ? 'fa-check-circle' : 'fa-exclamation-circle'}"></i>
                    <span>${message}</span>
                </div>
            `;
            document.body.appendChild(notification);
            setTimeout(() => notification.classList.add('show'), 10);
            setTimeout(() => {
                notification.classList.remove('show');
                setTimeout(() => notification.remove(), 300);
            }, 4000);
        };

        return {
            quoteForm,
            handleQuoteSubmit,
            isSubmitting
        };
    }
}
</script>

   
