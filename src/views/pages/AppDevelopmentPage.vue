    methods: {
        selectPlan(plan) {
            alert(`You selected the ${plan} plan!`);
        },
        toggleFAQ(btn) {
            var answer = btn.nextElementSibling;
            var isOpening = !answer.classList.contains('active');

            document.querySelectorAll('.faq-answer.active').forEach(function (el) {
                el.classList.remove('active');
            });

            document.querySelectorAll('.faq-question.active').forEach(function (q) {
                q.classList.remove('active');
            });

            if (isOpening) {
                btn.classList.add('active');
                answer.classList.add('active');
            }
        },
        initTestimonialsSlider() {
            const slider = document.querySelector('.testimonials-slider');
            const track = document.querySelector('.testimonials-slider-track');
            const cards = document.querySelectorAll('.testimonial-card');
            const prevBtn = document.querySelector('.slider-prev');
            const nextBtn = document.querySelector('.slider-next');
            const dotsContainer = document.querySelector('.slider-dots');

            if (!slider || cards.length === 0) return;

            let currentIndex = 0;
            let autoPlayInterval;
            let isDragging = false;
            let startX = 0;
            let currentTranslate = 0;
            let prevTranslate = 0;

            function getVisibleCount() {
                var w = window.innerWidth;
                if (w <= 600) return 1;
                if (w <= 992) return 2;
                return 3;
            }

            function getStepPercent() {
                var visible = getVisibleCount();
                var trackWidth = track.offsetWidth;
                var gap = 30;
                var cardWidth = (trackWidth - gap * (visible - 1)) / visible;
                return ((cardWidth + gap) / trackWidth) * 100;
            }

            function getMaxIndex() {
                return Math.max(0, cards.length - getVisibleCount());
            }

            // Build dots
            function buildDots() {
                dotsContainer.innerHTML = '';
                var count = getMaxIndex() + 1;
                for (var i = 0; i < count; i++) {
                    var dot = document.createElement('button');
                    dot.classList.add('slider-dot');
                    if (i === currentIndex) dot.classList.add('active');
                    dot.setAttribute('aria-label', 'Go to slide ' + (i + 1));
                    dot.addEventListener('click', (function (idx) {
                        return function () { goTo(idx); };
                    })(i));
                    dotsContainer.appendChild(dot);
                }
            }

            buildDots();

            function updateDots() {
                var dots = dotsContainer.querySelectorAll('.slider-dot');
                dots.forEach(function (d, i) {
                    d.classList.toggle('active', i === currentIndex);
                });
            }

            function goTo(index) {
                var maxIdx = getMaxIndex();
                if (index < 0) index = maxIdx;
                if (index > maxIdx) index = 0;
                currentIndex = index;
                currentTranslate = -currentIndex * getStepPercent();
                prevTranslate = currentTranslate;
                slider.style.transform = 'translateX(' + currentTranslate + '%)';
                updateDots();
                resetAutoPlay();
            }

            prevBtn.addEventListener('click', function () { goTo(currentIndex - 1); });
            nextBtn.addEventListener('click', function () { goTo(currentIndex + 1); });

            // Auto-play
            function startAutoPlay() {
                autoPlayInterval = setInterval(function () { goTo(currentIndex + 1); }, 5000);
            }
            function resetAutoPlay() {
                clearInterval(autoPlayInterval);
                startAutoPlay();
            }
            startAutoPlay();

            // Pause on hover
            slider.addEventListener('mouseenter', function () { clearInterval(autoPlayInterval); });
            slider.addEventListener('mouseleave', function () { startAutoPlay(); });

            // Touch / swipe support
            slider.addEventListener('touchstart', function (e) {
                isDragging = true;
                startX = e.touches[0].clientX;
                clearInterval(autoPlayInterval);
            }, { passive: true });

            slider.addEventListener('touchmove', function (e) {
                if (!isDragging) return;
                var diff = e.touches[0].clientX - startX;
                currentTranslate = prevTranslate + (diff / track.offsetWidth) * 100;
                slider.style.transform = 'translateX(' + currentTranslate + '%)';
                slider.style.transition = 'none';
            }, { passive: true });

            slider.addEventListener('touchend', function () {
                isDragging = false;
                slider.style.transition = '';
                var moved = currentTranslate - prevTranslate;
                if (moved < -8) goTo(currentIndex + 1);
                else if (moved > 8) goTo(currentIndex - 1);
                else goTo(currentIndex);
                startAutoPlay();
            });

            // Keyboard support
            document.addEventListener('keydown', function (e) {
                if (e.key === 'ArrowLeft') goTo(currentIndex - 1);
                if (e.key === 'ArrowRight') goTo(currentIndex + 1);
            });

            // Recalculate on resize
            window.addEventListener('resize', function () {
                buildDots();
                goTo(Math.min(currentIndex, getMaxIndex()));
            });
        },
    },
    mounted() {
        this.initTestimonialsSlider();
    },
};


