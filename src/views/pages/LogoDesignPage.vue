  full_name: '',
  email: '',
  phone: '',
  company: '',
  notes: ''
})

const openFaqIndex = ref(null)
const showScrollTop = ref(false)
const isSubmitting = ref(false)

const faqs = ref([
  {
    question: "What logo design services do you offer?",
    answer: "We offer complete logo design services including custom logo creation, brand identity packages, logo revisions, vector files delivery (AI, EPS, SVG, PNG, PDF), and brand guideline documents."
  },
